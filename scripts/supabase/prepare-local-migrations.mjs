// scripts/supabase/prepare-local-migrations.mjs - Genera migraciones locales de Supabase desde docs/supabase/sql para contributors.
import fs from "node:fs";
import path from "node:path";

const repositoryRoot = process.cwd();
const sourceDir = path.join(repositoryRoot, "docs", "supabase", "sql");
const targetDir = path.join(repositoryRoot, "supabase", "migrations");

function ensureDirectory(directoryPath) {
  fs.mkdirSync(directoryPath, { recursive: true });
}

function readSqlFilenames() {
  return fs
    .readdirSync(sourceDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith(".sql"))
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right));
}

function toMigrationPrefix(index) {
  const fixedDate = "202601010000";
  const sequence = String(index + 1).padStart(2, "0");
  return `${fixedDate}${sequence}`;
}

function cleanGeneratedMigrations() {
  if (!fs.existsSync(targetDir)) return;
  const files = fs.readdirSync(targetDir);
  for (const file of files) {
    if (!file.endsWith(".sql")) continue;
    const absolutePath = path.join(targetDir, file);
    const content = fs.readFileSync(absolutePath, "utf8");
    if (content.includes("Generated by scripts/supabase/prepare-local-migrations.mjs")) {
      fs.unlinkSync(absolutePath);
    }
  }
}

function writeMigrationFiles(filenames) {
  filenames.forEach((filename, index) => {
    const sourcePath = path.join(sourceDir, filename);
    const sql = fs.readFileSync(sourcePath, "utf8");
    const migrationFilename = `${toMigrationPrefix(index)}_${filename}`;
    const targetPath = path.join(targetDir, migrationFilename);
    const banner =
      `-- Generated by scripts/supabase/prepare-local-migrations.mjs\n` +
      `-- Source: docs/supabase/sql/${filename}\n\n`;
    fs.writeFileSync(targetPath, `${banner}${sql}`, "utf8");
  });
}

function main() {
  if (!fs.existsSync(sourceDir)) {
    throw new Error(`No existe el directorio fuente de SQL: ${sourceDir}`);
  }
  ensureDirectory(targetDir);
  cleanGeneratedMigrations();
  const sqlFilenames = readSqlFilenames();
  if (sqlFilenames.length === 0) throw new Error("No se encontraron archivos SQL en docs/supabase/sql.");
  writeMigrationFiles(sqlFilenames);
  console.log(`OK: ${sqlFilenames.length} migraciones locales generadas en supabase/migrations.`);
}

main();
